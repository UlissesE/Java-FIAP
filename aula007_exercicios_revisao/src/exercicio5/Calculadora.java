package exercicio5;

public class Calculadora {
	
	public int somar(int n1, int n2) {
		System.out.println(n1 + n2);
		return n1 + n2;
	}
	
	public double somar(double n1, double n2) {
		System.out.println(n1 + n2);
		return n1 + n2;
	}
	
	public String somar(String s1, String s2) {
		System.out.println(s1 + s2);
		return s1 + s2;
	}
	
}
